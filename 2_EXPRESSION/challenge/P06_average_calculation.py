#Given x and y, write an expression to calculate their average.
# Take first number as input and convert it to integer
x = int(input("Enter the number 1: "))

# Take second number as input and convert it to integer
y = int(input("Enter the number 2: "))

# Calculate average
average = (x + y) / 2   # average value is stored in 'average' variable

# Print the average value
print(average)
