#Given a and b, write an expression for their difference (a-b).
# Take first number as input and convert it to integer
a = int(input("Enter the number 1: "))

# Take second number as input and convert it to integer
b = int(input("Enter the number 2: "))

# Calculate absolute difference 
d = abs(a - b)   # absolute difference of a and b is stored in 'd'

# Print the result
print(d)
