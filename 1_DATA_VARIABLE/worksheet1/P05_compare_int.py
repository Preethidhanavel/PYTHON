#Compare two integers and print boolean results for each 
#comparison (==, !=, >, <, >=, <=).
x = int(input("Enter num1: "))  # Take first integer input from user
y = int(input("Enter num2: "))  # Take second integer input from user

print("x==y", x == y)   # Check if x is equal to y
print("x!=y", x != y)   # Check if x is not equal to y
print("x>y", x > y)     # Check if x is greater than y
print("x<y", x < y)     # Check if x is less than y
print("x>=y", x >= y)   # Check if x is greater than or equal to y
print("x<=y", x <= y)   # Check if x is less than or equal to y
