#Log Session integer, float, string, and boolean variables, and 
#use the type() function to print each data type.
x = 1        # Integer value
y = 2.54     # Float value
z = 'hello'  # String value
b = True     # Boolean value

print("x ", type(x))  # Print type of x (int)
print("y ", type(y))  # Print type of y (float)
print("z ", type(z))  # Print type of z (str)
print("b ", type(b))  # Print type of b (bool)
